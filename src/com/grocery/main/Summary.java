    private void btn_summaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_summaryActionPerformed
        // TODO add your handling code here:

        try {

            Summery_Between();

            JasperDesign jasdi = JRXmlLoader.load(SystemVariableList.REPORT_BASE__URL+"SummaryReport.jrxml");

            HashMap<String, Object> para = new HashMap<>();
            para.put("to_date", dateformat.format(from_date.getDate()));
            para.put("from_date", dateformat.format(to_date.getDate()));

            para.put("cash", cash_lb.getText().toString());
            para.put("card", card_lb.getText().toString());
            para.put("total", total_lb.getText().toString());

            para.put("sale", sales_lb.getText().toString());
            para.put("cost", cost_lb.getText().toString());
            para.put("profit", profit_lb.getText().toString());

            JasperReport js = JasperCompileManager.compileReport(jasdi);
            JasperPrint jp = JasperFillManager.fillReport(js, para, DB.getDBConnection());
            JasperExportManager.exportReportToPdfFile(jp, SystemVariableList.PDF_BASE_URL+"Summary\\Summary.pdf");
            JasperViewer.viewReport(jp, false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, String.valueOf(e), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_summaryActionPerformed

