    void Summery_Between() {

        try {
            cash_total = 0;
            cash_costtotal = 0;
            card_total = 0;
            card_costtotal = 0;
            both_total = 0;
            both_costtotal = 0;
            sales =0;

            Clear();
            String from_day = dateformat.format(from_date.getDate()).toString();
            String to_day = dateformat.format(to_date.getDate()).toString();
            
            System.out.println(to_day.trim());
            System.out.println(from_day.trim());

            ResultSet rs = DB.search("select * from invoice where DATE(currentdate) BETWEEN DATE('"+from_day+"') AND  DATE('"+to_day+"')");
            while (rs.next()) {

                String type = rs.getString("cus_type");

                if (type.trim().equals("Cash Customer")) {
                    cash_total += rs.getDouble("nettotal");
                    cash_costtotal += rs.getDouble("costtotal");
                }
            }

            cash_lb.setText("Rs " + cash_total);

            ResultSet rs2 = DB.search("select * from invoice where DATE(currentdate) BETWEEN DATE('"+from_day+"') AND  DATE('"+to_day+"')");
            while (rs2.next()) {

                String type = rs2.getString("cus_type");

                if (type.trim().equals("Card Customer")) {
                    card_total += rs2.getDouble("nettotal");
                    card_costtotal += rs2.getDouble("costtotal");
                }
            }

            card_lb.setText("RS " + card_total);
            both_total = cash_total + card_total;
            both_costtotal = cash_costtotal + card_costtotal;
            total_lb.setText("Rs " + both_total);

            ResultSet rs3 = DB.search("select * from invoice where DATE(currentdate) BETWEEN DATE('"+from_day+"') AND  DATE('"+to_day+"')");
            while (rs3.next()) {

                sales++;
            }

            sales_lb.setText(String.valueOf(sales));

            profit_lb.setText("Rs " + (both_total - both_costtotal));

            cost_lb.setText("Rs " + both_costtotal);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, String.valueOf(e), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

