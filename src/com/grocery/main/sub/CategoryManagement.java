    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        try {
            String catid = combo_idsearch.getSelectedItem().toString();
            ResultSet rs = DB.search("select * from category where cat_id = '" + catid + "'");
            if (rs.next()) {
                boolean status = rs.getBoolean("cat_status");
                if (status) {
                    
                    txt_catid.setText(rs.getString("cat_id"));
                    txt_catname.setText(rs.getString("cat_name"));
                    txt_catdescription.setText(rs.getString("cat_description"));
                    combo_catstatus.setSelectedItem("cat_status");

                } else {
                    JOptionPane.showMessageDialog(this, "Category is not Active", "error", JOptionPane.ERROR_MESSAGE);

                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_btn_searchActionPerformed

    String st1;
    private void btn_add1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add1ActionPerformed
        String id = txt_catid.getText();
        String name = txt_catname.getText();
        String description = txt_catdescription.getText();
        String status = combo_catstatus.getSelectedItem().toString();
        if (status.trim().equals("Active")) {
            int st = 1;
            st1 = "" + st;
        } else {
            int st = 0;
            st1 = "" + st;
        }

        try {
            DB.push("insert into category  values('" + id + "','" + name + "','" + description + "','" + st1 + "')");
            JOptionPane.showMessageDialog(this, "category  saved successfully", "success", JOptionPane.INFORMATION_MESSAGE);
            empidgenerate();
            idSearch();
            Clear();
            Category c = new Category();
            c.addDataToTable();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btn_add1ActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        String status = combo_catstatus.getSelectedItem().toString();
        if (status.trim().equals("Active")) {
            int st = 1;
            st1 = "" + st;
        } else {
            int st = 0;
            st1 = "" + st;
        }
        try {
            DB.push("update category set cat_name = '" + txt_catname.getText() + "', cat_description = '" + txt_catdescription.getText() + "', cat_status='" + st1 + "'  where cat_id = '" + txt_catid.getText() + "' ");
            JOptionPane.showMessageDialog(this, "Category updated success", "success", JOptionPane.INFORMATION_MESSAGE);
            empidgenerate();
            Clear();
        } catch (Exception e) {
           JOptionPane.showMessageDialog(this, String.valueOf(e), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_updateActionPerformed

