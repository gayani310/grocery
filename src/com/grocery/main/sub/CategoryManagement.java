    private void idSearch() {
        try {
            ResultSet rs = DB.search("select * from category where cat_status = 1");
            Vector v = new Vector();

            while (rs.next()) {
                String data = rs.getString("cat_id");
                v.add(data);
            }

            combo_idsearch.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

