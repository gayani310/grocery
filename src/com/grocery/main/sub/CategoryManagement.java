    private void empidgenerate() {
        try {

            ResultSet rs = DB.search("select count(*) as empid from category");
            if (rs.next()) {
                int Count = rs.getInt("empid");
                String id = "CAT" + (++Count);
                txt_catid.setText(id);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

