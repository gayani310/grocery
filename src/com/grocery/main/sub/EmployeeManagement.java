    private void empidgenerate() {
        try {

            ResultSet rs = DB.search("select count(*) as empid from employee");
            if (rs.next()) {
                int Count = rs.getInt("empid");
                String id = "EMP" + (++Count);
                txt_id.setText(id);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, String.valueOf(e), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void idSearch() {
        try {
            ResultSet rs = DB.search("select * from employee where emp_status = 1");
            Vector v = new Vector();

            while (rs.next()) {
                String data = rs.getString("emp_id");
                v.add(data);
            }

            combo_idsearch.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, String.valueOf(e), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

