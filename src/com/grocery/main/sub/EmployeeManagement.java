    private void combo_idsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_idsearchActionPerformed
        // TODO add your handling code here:

        try {
            String empid = combo_idsearch.getSelectedItem().toString();
            ResultSet rs = DB.search("select * from employee where emp_id = '" + empid + "'");
            if (rs.next()) {
                boolean status = rs.getBoolean("emp_status");
                if (status) {
                    txt_id.setText(rs.getString("emp_id"));
                    txt_name.setText(rs.getString("emp_name"));
                    combo_utype.setSelectedItem(rs.getString("user_type"));
                    txt_username.setText(rs.getString("emp_username"));
                    txt_contactno.setText(rs.getString("emp_contctno"));
                    txt_address.setText(rs.getString("emp_address"));
                    txt_password.setEditable(false);
                    txt_confirmpass.setEditable(false);

                } else {
                    JOptionPane.showMessageDialog(this, "Employee is not  Active", "error", JOptionPane.ERROR_MESSAGE);

                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, String.valueOf(e), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_combo_idsearchActionPerformed

