    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        String id = txt_id.getText();
        String name = txt_name.getText();
        String combo_type = combo_utype.getSelectedItem().toString();
        String uname = txt_username.getText();
        String contectno = txt_contactno.getText();
        String pass1 = new String(txt_password.getPassword());
        String mc1 = MD5.getMd5(pass1);
        String pass2 = new String(txt_confirmpass.getPassword());
        String mc2 = MD5.getMd5(pass2);
        String addr = txt_address.getText();
        try {
            if (mc1.trim().equals(mc2)) {
                DB.push("insert into employee  values('" + id + "','" + name + "','" + combo_type + "','" + uname + "','" + contectno + "','" + mc1 + "','" + addr + "','1')");
                JOptionPane.showMessageDialog(this, "data save success", "success", JOptionPane.INFORMATION_MESSAGE);
                empidgenerate();
                Clear();
                idSearch();
            } else {
                JOptionPane.showMessageDialog(this, "password wrong", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, String.valueOf(e), "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btn_addActionPerformed

