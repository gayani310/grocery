    private void empidgenerate() {
        try {

            ResultSet rs = DB.search("select count(*) as cusid from customer");
            if (rs.next()) {
                int Count = rs.getInt("cusid");
                String id = "CUS" + (++Count);
                txt_id.setText(id);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void idSearch() {
        try {
            ResultSet rs = DB.search("select * from customer");
            Vector v = new Vector();

            while (rs.next()) {
                String data = rs.getString("cus_id");
                v.add(data);
            }

            combo_idsearch.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

