    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
         try {
            String empid = combo_idsearch.getSelectedItem().toString();
            ResultSet rs = DB.search("select * from customer where cus_id = '" + combo_idsearch.getSelectedItem() + "'");
            if (rs.next()) {
                boolean status = rs.getBoolean("cus_status");
                if (status) {
                    txt_id.setText(rs.getString("cus_id"));
                    txt_name.setText(rs.getString("cus_name"));
                    combo_ctype.setSelectedItem(rs.getString("cus_type"));
                    txt_contactno.setText(rs.getString("cus_contact"));
                    txt_address.setText(rs.getString("cus_address"));


                } else {
                    JOptionPane.showMessageDialog(this, "Customer is not  Active", "error", JOptionPane.ERROR_MESSAGE);

                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_searchActionPerformed

